    // * : 0개 이상
    // ? : 0, 1
    // ^ : ~로 시작
    // $ : ~로 끝
    // . : 임의의 문자
    // [0-9a-zA-Z] : \w
    // [0-9] : \d
    // [^] : 제외

    // 010-1234-5678
    String number = "([01]{3})-(\\d{3,4})-(\\d{4})";

    // abcd@naver.com
